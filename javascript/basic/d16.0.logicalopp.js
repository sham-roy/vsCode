// logical operators
//    &&    ||       !
console.log(2>5 && 6<2);

// true  && true  = true
// true  && false = false
// false && false = false
// false && false = false

console.log(2<5 || 6>3);

// false || false = false
// true  || false = true
// false || true  = true
// true  || true  = true

console.log(!(5>2));
console.log(!(2<5 || 6>3));

